      <h2>1. Çocukların Cinsel İstismarı ve Sömürüsüne (CSAE) Karşı Sıfır Tolerans</h2>
      <p>Loopn, Çocukların Cinsel İstismarı ve Sömürüsünü (Child Sexual Abuse and Exploitation - CSAE) kesinlikle ve açıkça yasaklar. Uygulamamız üzerinden her türlü çocuk istismarı materyali (CSAM) paylaşımı, oluşturulması, talep edilmesi veya herhangi bir şekilde sömürü faaliyetine zemin hazırlanması <strong>kesinlikle yasaktır</strong>.</p>
      
      <h2>2. Kullanıcı Yaş Sınırı</h2>
      <p>Loopn uygulaması yalnızca 18 yaş ve üzeri kullanıcılar içindir. 18 yaşından küçüklerin uygulamamızı kullanması yasaktır. Tespit edilen veya rapor edilen reşit olmayan hesaplar derhal kapatılır.</p>

      <h2>3. Bildirim, Moderasyon ve Uygulama İçi Şikayet</h2>
      <p>Kullanıcılarımız, uygulama içerisindeki herhangi bir şüpheli profili veya mesajı doğrudan rapor etme (Report) ve engelleme (Block) seçeneğine sahiptir. Uygulama içerisindeki rapor mekanizması aracılığıyla CSAE şüphesi barındıran içerikler derhal moderatör ekibimiz tarafından incelenir ve hesaplar sistemden kalıcı olarak silinir.</p>

      <h2>4. Yasal Yetkililerle İşbirliği</h2>
      <p>CSAE politikamızı ihlal eden herhangi bir içerik veya faaliyet tespit edildiğinde, hesaplar yalnızca kalıcı olarak yasaklanmakla kalmaz, aynı zamanda NCMEC (National Center for Missing & Exploited Children) dahil olmak üzere ilgili ulusal ve uluslararası emniyet teşkilatlarına derhal rapor edilir.</p>

